package xd.simu.vo;

public class TeacherVo {
	String teachPword;
	String teachName;
	String teachNum;
	String teachGender;
	String teachPosition;
	String remark;
	int teachAge;
	public int getTeachAge() {
		return teachAge;
	}
	public void setTeachAge(int teachAge) {
		this.teachAge = teachAge;
	}
	public String getRemark() {
		return remark;
	}
	public void setRemark(String remark) {
		this.remark = remark;
	}
	public String getTeachPword() {
		return teachPword;
	}
	public void setTeachPword(String teachPword) {
		this.teachPword = teachPword;
	}
	public String getTeachName() {
		return teachName;
	}
	public void setTeachName(String teachName) {
		this.teachName = teachName;
	}
	public String getTeachNum() {
		return teachNum;
	}
	public void setTeachNum(String teachNum) {
		this.teachNum = teachNum;
	}
	public String getTeachGender() {
		return teachGender;
	}
	public void setTeachGender(String teachGender) {
		this.teachGender = teachGender;
	}
	public String getTeachPosition() {
		return teachPosition;
	}
	public void setTeachPosition(String teachPosition) {
		this.teachPosition = teachPosition;
	}
	
}
