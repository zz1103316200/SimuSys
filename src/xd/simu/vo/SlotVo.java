package xd.simu.vo;

public class SlotVo {
	String slotStyle;
	
	int slotID;
	String remark;
	public String getSlotStyle() {
		return slotStyle;
	}
	public void setSlotStyle(String slotStyle) {
		this.slotStyle = slotStyle;
	}
	public int getSlotID() {
		return slotID;
	}
	public void setSlotID(int slotID) {
		this.slotID = slotID;
	}
	public String getRemark() {
		return remark;
	}
	public void setRemark(String remark) {
		this.remark = remark;
	}
}
